import { api } from "../../convex/_generated/api";
import type { Id } from "../../convex/_generated/dataModel";

/**
 * Generate a comprehensive patient report PDF
 */
export async function generatePatientReportPDFFile(reportData: any, filename: string, diabetesStatus?: string) {
  // Get diabetes status from reportData if not provided
  const patientDiabetesStatus = diabetesStatus || reportData.patient?.diabetesStatus || "none";
  const printWindow = window.open("", "_blank");
  if (!printWindow) {
    throw new Error("Unable to open print window. Please allow popups for this site.");
  }

  const formatDate = (timestamp: number | undefined) => {
    if (!timestamp) return "N/A";
    return new Date(timestamp).toLocaleDateString("en-GB", {
      day: "2-digit",
      month: "long",
      year: "numeric",
    });
  };

  const formatDateTime = (timestamp: number | undefined) => {
    if (!timestamp) return "N/A";
    const date = new Date(timestamp);
    return date.toLocaleString("en-GB", {
      day: "2-digit",
      month: "short",
      year: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    });
  };

  const riskColor = (category: string) => {
    switch (category) {
      case "very_high": return "#EF4444";
      case "high": return "#F97316";
      case "moderate": return "#EAB308";
      case "low": return "#22C55E";
      default: return "#64748b";
    }
  };

  const riskLabel = (category: string) => {
    return category?.replace("_", " ").toUpperCase() || "UNKNOWN";
  };

  printWindow.document.write(`
    <!DOCTYPE html>
    <html>
    <head>
      <title>Patient Health Report - ${reportData.patient.name}</title>
      <style>
        @media print {
          @page {
            margin: 1cm;
          }
        }
        body {
          font-family: 'Segoe UI', Tahoma, Geneva, Verdana, sans-serif;
          margin: 0;
          padding: 40px;
          color: #1e293b;
          background: white;
          line-height: 1.6;
        }
        .header {
          border-bottom: 4px solid #3B82F6;
          padding-bottom: 20px;
          margin-bottom: 30px;
        }
        .header h1 {
          margin: 0;
          color: #3B82F6;
          font-size: 32px;
        }
        .header-info {
          display: flex;
          justify-content: space-between;
          margin-top: 15px;
          font-size: 14px;
          color: #64748b;
        }
        .section {
          margin-bottom: 40px;
          page-break-inside: avoid;
        }
        .section-title {
          font-size: 24px;
          font-weight: bold;
          color: #1e293b;
          margin-bottom: 20px;
          padding-bottom: 10px;
          border-bottom: 2px solid #e2e8f0;
        }
        .summary-grid {
          display: grid;
          grid-template-columns: repeat(2, 1fr);
          gap: 20px;
          margin-bottom: 30px;
        }
        .summary-card {
          background: #f8fafc;
          padding: 20px;
          border-radius: 8px;
          border-left: 4px solid #3B82F6;
        }
        .summary-label {
          font-size: 14px;
          color: #64748b;
          margin-bottom: 8px;
        }
        .summary-value {
          font-size: 28px;
          font-weight: bold;
          color: #1e293b;
        }
        table {
          width: 100%;
          border-collapse: collapse;
          margin-top: 15px;
        }
        th, td {
          padding: 12px;
          text-align: left;
          border-bottom: 1px solid #e2e8f0;
        }
        th {
          background: #f1f5f9;
          font-weight: 600;
          color: #475569;
        }
        .risk-badge {
          display: inline-block;
          padding: 4px 12px;
          border-radius: 12px;
          font-size: 12px;
          font-weight: 600;
          color: white;
        }
        .medication-item {
          background: #f8fafc;
          padding: 15px;
          border-radius: 8px;
          margin-bottom: 10px;
        }
        .footer {
          margin-top: 50px;
          padding-top: 20px;
          border-top: 2px solid #e2e8f0;
          text-align: center;
          color: #64748b;
          font-size: 12px;
        }
      </style>
    </head>
    <body>
      <div class="header">
        <h1>Patient Health Report</h1>
        <div class="header-info">
          <div>
            <strong>Patient:</strong> ${reportData.patient.name}<br>
            ${reportData.patient.dateOfBirth ? `<strong>Date of Birth:</strong> ${reportData.patient.dateOfBirth}<br>` : ''}
            ${reportData.patient.gender ? `<strong>Gender:</strong> ${reportData.patient.gender.charAt(0).toUpperCase() + reportData.patient.gender.slice(1)}<br>` : ''}
          </div>
          <div>
            <strong>Generated By:</strong> Dr. ${reportData.doctor.name}<br>
            ${reportData.doctor.specialization ? `<strong>Specialization:</strong> ${reportData.doctor.specialization}<br>` : ''}
            <strong>Date:</strong> ${formatDate(reportData.generatedAt)}
          </div>
        </div>
      </div>

      <div class="section">
        <div class="section-title">Executive Summary</div>
        <div class="summary-grid">
          <div class="summary-card">
            <div class="summary-label">Total Assessments</div>
            <div class="summary-value">${reportData.summary.totalAssessments}</div>
          </div>
          <div class="summary-card">
            <div class="summary-label">${(() => {
              const hasDiagnosedDiabetes = patientDiabetesStatus === "type1" || patientDiabetesStatus === "type2" || 
                                          patientDiabetesStatus === "gestational" || patientDiabetesStatus === "other";
              const isPrediabetic = patientDiabetesStatus === "prediabetic";
              return hasDiagnosedDiabetes 
                ? "Current Complication Risk" 
                : isPrediabetic 
                ? "Current Progression Risk" 
                : "Current Risk Score";
            })()}</div>
            <div class="summary-value" style="color: ${riskColor(reportData.summary.latestRiskCategory)}">
              ${reportData.summary.latestRiskScore.toFixed(1)}%
            </div>
          </div>
          <div class="summary-card">
            <div class="summary-label">${(() => {
              const hasDiagnosedDiabetes = patientDiabetesStatus === "type1" || patientDiabetesStatus === "type2" || 
                                          patientDiabetesStatus === "gestational" || patientDiabetesStatus === "other";
              const isPrediabetic = patientDiabetesStatus === "prediabetic";
              return hasDiagnosedDiabetes 
                ? "Complication Category" 
                : isPrediabetic 
                ? "Progression Category" 
                : "Risk Category";
            })()}</div>
            <div class="summary-value" style="color: ${riskColor(reportData.summary.latestRiskCategory)}">
              ${riskLabel(reportData.summary.latestRiskCategory)}
            </div>
          </div>
          <div class="summary-card">
            <div class="summary-label">Assessment Period</div>
            <div class="summary-value" style="font-size: 18px;">
              ${formatDate(reportData.summary.firstAssessmentDate)} - ${formatDate(reportData.summary.lastAssessmentDate)}
            </div>
          </div>
        </div>
        ${reportData.summary.riskTrend !== 0 ? `
          <p style="margin-top: 20px; padding: 15px; background: ${reportData.summary.riskTrend > 0 ? '#FEE2E2' : '#D1FAE5'}; border-radius: 8px;">
            <strong>Risk Trend:</strong> ${reportData.summary.riskTrend > 0 ? '↑ Increasing' : '↓ Decreasing'} 
            (${Math.abs(reportData.summary.riskTrend).toFixed(1)}% change)
          </p>
        ` : ''}
      </div>

      <div class="section">
        <div class="section-title">Risk Distribution</div>
        <table>
          <thead>
            <tr>
              <th>Risk Level</th>
              <th>Count</th>
              <th>Percentage</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td><span class="risk-badge" style="background: #22C55E;">Low</span></td>
              <td>${reportData.summary.riskDistribution.low}</td>
              <td>${reportData.summary.totalAssessments > 0 ? ((reportData.summary.riskDistribution.low / reportData.summary.totalAssessments) * 100).toFixed(1) : 0}%</td>
            </tr>
            <tr>
              <td><span class="risk-badge" style="background: #EAB308;">Moderate</span></td>
              <td>${reportData.summary.riskDistribution.moderate}</td>
              <td>${reportData.summary.totalAssessments > 0 ? ((reportData.summary.riskDistribution.moderate / reportData.summary.totalAssessments) * 100).toFixed(1) : 0}%</td>
            </tr>
            <tr>
              <td><span class="risk-badge" style="background: #F97316;">High</span></td>
              <td>${reportData.summary.riskDistribution.high}</td>
              <td>${reportData.summary.totalAssessments > 0 ? ((reportData.summary.riskDistribution.high / reportData.summary.totalAssessments) * 100).toFixed(1) : 0}%</td>
            </tr>
            <tr>
              <td><span class="risk-badge" style="background: #EF4444;">Very High</span></td>
              <td>${reportData.summary.riskDistribution.veryHigh}</td>
              <td>${reportData.summary.totalAssessments > 0 ? ((reportData.summary.riskDistribution.veryHigh / reportData.summary.totalAssessments) * 100).toFixed(1) : 0}%</td>
            </tr>
          </tbody>
        </table>
      </div>

      <div class="section">
        <div class="section-title">Assessment History</div>
        <table>
          <thead>
            <tr>
              <th>Date</th>
              <th>${(() => {
                const hasDiagnosedDiabetes = patientDiabetesStatus === "type1" || patientDiabetesStatus === "type2" || 
                                            patientDiabetesStatus === "gestational" || patientDiabetesStatus === "other";
                const isPrediabetic = patientDiabetesStatus === "prediabetic";
                return hasDiagnosedDiabetes 
                  ? "Complication Risk" 
                  : isPrediabetic 
                  ? "Progression Risk" 
                  : "Risk Score";
              })()}</th>
              <th>Category</th>
              <th>Confidence</th>
            </tr>
          </thead>
          <tbody>
            ${reportData.assessments.map((assessment: any) => `
              <tr>
                <td>${formatDateTime(assessment.date)}</td>
                <td><strong>${assessment.riskScore.toFixed(1)}%</strong></td>
                <td><span class="risk-badge" style="background: ${riskColor(assessment.riskCategory)};">${riskLabel(assessment.riskCategory)}</span></td>
                <td>${assessment.confidenceScore ? assessment.confidenceScore.toFixed(1) + '%' : 'N/A'}</td>
              </tr>
            `).join('')}
          </tbody>
        </table>
      </div>

      ${reportData.medications && reportData.medications.length > 0 ? `
        <div class="section">
          <div class="section-title">Active Medications</div>
          ${reportData.medications.map((med: any) => `
            <div class="medication-item">
              <strong>${med.name}</strong> - ${med.dosage}<br>
              <small>Frequency: ${med.frequency.replace('_', ' ')}</small>
              ${med.notes ? `<br><small>Notes: ${med.notes}</small>` : ''}
            </div>
          `).join('')}
        </div>
      ` : ''}

      <div class="footer">
        <p>This report was generated on ${formatDateTime(reportData.generatedAt)}</p>
        <p>Diabetes Risk Prediction System - Confidential Medical Document</p>
      </div>
    </body>
    </html>
  `);

  printWindow.document.close();
  setTimeout(() => {
    printWindow.print();
  }, 250);
}



